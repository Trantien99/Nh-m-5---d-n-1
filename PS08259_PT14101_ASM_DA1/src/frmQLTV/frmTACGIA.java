    private void tblTACGIAMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTACGIAMouseClicked
        try {
            Tacgia tg = list.get(tblTACGIA.getSelectedRow());
            txtMATG.setText(tg.getMatacgia());
            txtTENTG.setText(tg.getTentacgia());
            if (tg.getGhichu().equals("1")) {
                txtTGGHICHU.setText("");
            } else {
                txtTGGHICHU.setText(tg.getGhichu());
            }
            btnTGHINH.setIcon(new ImageIcon(getClass().getResource("/IMGTG/" + tg.getHinhanh())));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Dữ liệu trống");
        }
    }//GEN-LAST:event_tblTACGIAMouseClicked

