    private void fillToTableTacgia() {
        DefaultTableModel model = (DefaultTableModel) tblTACGIA.getModel();
        model.setRowCount(0);
        for (Tacgia tg : list) {
            Object[] row = new Object[]{tg.getMatacgia(), tg.getTentacgia(), tg.getGhichu(), tg.getHinhanh()};
            model.addRow(row);
        }
    }

