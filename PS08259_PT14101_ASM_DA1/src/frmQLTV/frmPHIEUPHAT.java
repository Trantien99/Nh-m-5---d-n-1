    private void tblPHIEUPHATMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPHIEUPHATMouseClicked
        try {
            DecimalFormat formatter = new DecimalFormat("###,###,###");
            PhieuphatBS pp = list.get(tblPHIEUPHAT.getSelectedRow());
            txtMAPM.setText(pp.getMaphieumuon());
            txtMAPP.setText(pp.getMapp());
            txtMADG.setText(pp.getMadg());
            txtNGAYHETHAN.setText(pp.getNgaytra());
            txtSONGAY.setText(pp.getSongayhethan());
            if (pp.getTrangthai().equals("0")) {
                rdbDTT.setSelected(true);
            } else {
                rdbCTT.setSelected(true);
            }
            int tp = Integer.parseInt(pp.getTienphat());
            txtTENPHAT.setText(formatter.format(tp));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Dữ liệu trống");
        }
    }//GEN-LAST:event_tblPHIEUPHATMouseClicked

