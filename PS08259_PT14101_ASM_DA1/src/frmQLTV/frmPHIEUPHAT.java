    private void capnhatPhieuphat() {
        String mapp = txtMAPP.getText();
        String tt;
        if (rdbDTT.isSelected()) {
            tt = "0";
        } else {
            tt = "1";
        }
        PhieuphatDao.suaPhieuphat(mapp, tt);
        JOptionPane.showMessageDialog(this, "Cập nhật thành công");

    }

    private void timkiemPhieuphat() {
        String sql = "";
        if (rdbMPM.isSelected()) {
            sql = " and p.mapm like '%" + txtMAPM.getText() + "%'";
        } else if (rdbMDG.isSelected()) {
            sql = " and m.madg like '%" + txtMADG.getText() + "%'";
        } else if (rdbTT.isSelected()) {
            String tt = "";
            if (rdbDTT.isSelected()) {
                tt = "0";
            } else {
                tt = "1";
            }
            sql = " and p.trangthai like '%" + tt + "%'";
        }
        laydulieuPhieuphat(sql);
    }

