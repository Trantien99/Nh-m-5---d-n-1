    private void laydulieuPhieuphat(String sql) {
        try {
            list.clear();
            tblPHIEUPHAT.removeAll();
            list = PhieuphatDao.layttPhieuphatCT(sql);
            fillToTablePhieuphat();
            laydulieuPP();
        } catch (SQLException ex) {
            Logger.getLogger(frmPHIEUPHAT.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void fillToTablePhieuphat() {
        DefaultTableModel model = (DefaultTableModel) tblPHIEUPHAT.getModel();
        model.setRowCount(0);
        for (PhieuphatBS pp : list) {
            String tt;
            if (pp.getTrangthai().equals("0")) {
                tt = "Đã thanh toán";
            } else {
                tt = "Chưa thanh toán";
            }
            Object[] row = new Object[]{pp.getMapp(), pp.getMaphieumuon(), pp.getMadg(), pp.getNgaytra(), pp.getSongayhethan(), pp.getTienphat(), tt};
            model.addRow(row);
        }
    }

    private void laydulieuPP() {
        try {
            DecimalFormat formatter = new DecimalFormat("###,###,###");
            this.currentindex = 0;
            if (list.size() == 1) {
                this.currentindex = 0;
            }
            PhieuphatBS pp = list.get(currentindex);
            txtMAPM.setText(pp.getMaphieumuon());
            txtMAPP.setText(pp.getMapp());
            txtMADG.setText(pp.getMadg());
            txtNGAYHETHAN.setText(pp.getNgaytra());
            txtSONGAY.setText(pp.getSongayhethan());
            if (pp.getTrangthai().equals("0")) {
                rdbDTT.setSelected(true);
            } else {
                rdbCTT.setSelected(true);
            }
            int tp = Integer.parseInt(pp.getTienphat());
            txtTENPHAT.setText(formatter.format(tp));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Dữ liệu trống");
        }
    }

